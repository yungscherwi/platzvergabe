<h1>  FAQ </h1>
<br><br>
<h2> Was ist die Funktion dieser Seite? </h2>
<p>Diese Seite erstellt automatisiert Sitzpläne für Prüfungen der Georg-August-Universität im zentralen Hörsaalgebäude (ZHG).</p>
<br>
<h2> Wie funktioniert das ganze? </h2>
<p>Zunächst gehen Sie auf den Reiter Home, dort können Sie den für die Klausur gewünschten Hörsaal auswählen. Dann laden Sie die .csv Datei mit den teilnehmenden Studenten hoch und klicken auf Abschicken. Sie gelangen dann zum erstellten Sitzplan und können diesen und die Anwesenheitsliste speichern, sowie drucken. </p>
<br>
<h2> Woher weiß ich wieviel Plätze ein Hörsaal hat? </h2>
<p>Eine Übersicht über die Kapazitäten der einzelnen Hörsäle ist unter dem Reiter "Hörsäle" zu finden. </p>
<br>
<h2> Wieso unterscheiden sich die Sitzplätze in der Klausur von der gesamten Kapazität? </h2>
<p> Um ein Abschreiben zu verindern, wird nur jede zweite Reihe mit Studenten belegt und zudem nur jeder 3. Platz. Daraus ergibt sich eine große Differenz zwischen gesamter Kapazität und Sitzplatzanzahl in einer Klausur.</p>
